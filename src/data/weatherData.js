export function getNavResult(naturalRoll, totalRoll, dc) {
  if (naturalRoll === 1)  return NAV_RESULTS[0]
  if (naturalRoll === 20) return NAV_RESULTS[5]
  const diff = totalRoll - dc
  if (diff >= 0)  return NAV_RESULTS[4]
  if (diff >= -3) return NAV_RESULTS[3]
  if (diff >= -6) return NAV_RESULTS[2]
  return NAV_RESULTS[1]
}

