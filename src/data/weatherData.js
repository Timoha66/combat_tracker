export function rollNextWeather(currentKey, stability) {
  const level = STABILITY_LEVELS.find(s => s.id === stability) ?? STABILITY_LEVELS[2]
  const d6 = Math.floor(Math.random() * 6) + 1
  if (d6 <= level.holdChance) return currentKey
  const currentIdx = WEATHER_SCALE.indexOf(currentKey)
  const shift = Math.floor(Math.random() * (level.shift * 2 + 1)) - level.shift
  const newIdx = Math.max(0, Math.min(WEATHER_SCALE.length - 1, currentIdx + shift))
  return WEATHER_SCALE[newIdx]
}

export function rollAnchorWeather(anchorKey) {
  const d6 = Math.floor(Math.random() * 6) + 1
  if (d6 <= 4) return anchorKey
  const idx = WEATHER_SCALE.indexOf(anchorKey)
  const shift = Math.random() < 0.5 ? -1 : 1
  const newIdx = Math.max(0, Math.min(WEATHER_SCALE.length - 1, idx + shift))
  return WEATHER_SCALE[newIdx]
}

export function randomFlavour(weatherKey) {
  const flavours = WEATHER[weatherKey]?.flavour ?? []
  return flavours[Math.floor(Math.random() * flavours.length)] ?? ''
}
